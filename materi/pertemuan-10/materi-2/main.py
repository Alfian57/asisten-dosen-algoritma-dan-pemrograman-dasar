import math
import random

# Menghitung akar kuadrat
print(math.sqrt(36))
print(math.sqrt(100))
print(math.sqrt(121))

# Menghitung pangkat
print(math.pow(5, 3))
print(math.pow(3, 3))
print(math.pow(2, 10))

# Pembulatan ke atas
print(math.ceil(9.7))
print(math.ceil(11.1))
print(math.ceil(12.9))

# menghasilkan bilangan float acak dari 0 sampai 1
print(random.random())

# Menghasilkan bilangan acak antara 1 sampai 100
print(random.randint(1, 100))