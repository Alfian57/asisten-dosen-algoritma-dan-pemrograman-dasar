# Konversi Jarak (KM to Meter & CM)
# Tujuan: Mengubah jarak kilometer ke meter dan centimeter

jarak_km = float(input("Masukkan jarak dalam KM: "))

jarak_meter = jarak_km * 1000
jarak_cm = jarak_meter * 100

print(f"{jarak_km} KM = {jarak_meter} meter")
print(f"{jarak_km} KM = {jarak_cm} centimeter")
